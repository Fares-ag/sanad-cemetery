  static String tr(BuildContext context, String key, [String? arg1]) {
    final s = trRaw(context, key);
    if (arg1 != null && s.contains('%s')) return s.replaceFirst('%s', arg1);
    return s;
  }

  static String trRaw(BuildContext context, String key) {
    final locale = context.watch<LocaleProvider>();
    final map = locale.isArabic ? _ar : _en;
    return map[key] ?? _en[key] ?? key;
  }

  static String trOptional(BuildContext context, String key) {
    return trRaw(context, key);
  }

  static String status(BuildContext context, TicketStatus status) {
    switch (status) {
      case TicketStatus.reported:
        return tr(context, 'statusReported');
      case TicketStatus.inProgress:
        return tr(context, 'statusInProgress');
      case TicketStatus.resolved:
        return tr(context, 'statusResolved');
    }
  }

  static String categoryDisplay(BuildContext context, String categoryKeyOrRaw) {
    final key = categoryKeyOrRaw;
    final keyMap = {
      'sunkenGrave': 'sunkenGrave',
      'Sunken Grave': 'sunkenGrave',
      'damagedStone': 'damagedStone',
      'Damaged Stone': 'damagedStone',
      'overgrownGrass': 'overgrownGrass',
      'Overgrown Grass': 'overgrownGrass',
      'other': 'other',
      'Other': 'other',
    };
    final trKey = keyMap[key];
    if (trKey != null) return tr(context, trKey);
    return key;
  }
